
# Pasar datos por pantalla con Print
print("Hola Mundo")
print("Hola cómo estás?")

# Diferencia entre pasar datos por pantalla, con un sep
# El sep, añade a la cadena de texto el caracter que se le pase
print(1,2,3,4,5)
print(1,2,3,4,5, sep=",")

# El end añade únicamente el caracter que se le pase al final de la salida del print()
print(1,2,3,4,5, end=",")